x = 20
# 変数 x が 10 以上 30 以下の場合に「 xは10以上30以下です 」と出力してください
if x >= 10 and x <= 30:
    print('xは10以上30以下です')

y = 60
# 変数 y が 10 未満または 30 より大きい場合に「 yは10未満または30より大きいです 」と出力してください
if y < 10 or y > 30:
    print('yは10未満または30より大きいです')

z = 55
